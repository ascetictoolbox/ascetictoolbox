package eu.ascetic.saas.experimentmanager.exception;

public class MetricDefinitionIncorrectException extends Exception {
	

	/**
	 * 
	 */
	private static final long serialVersionUID = -5034563191530297034L;


	public MetricDefinitionIncorrectException() {
		super();
	}

	public MetricDefinitionIncorrectException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public MetricDefinitionIncorrectException(String message, Throwable cause) {
		super(message, cause);
	}

	public MetricDefinitionIncorrectException(String message) {
		super(message);
	}

	public MetricDefinitionIncorrectException(Throwable e) {
		super(e);
	}

}
