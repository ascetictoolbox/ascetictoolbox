package eu.ascetic.saas.experimentmanager.wslayer.exception;

public class IncorrectRessourceFormatException extends WSBaseException {
	

	/**
	 * 
	 */
	private static final long serialVersionUID = -5034563191530297034L;


	public IncorrectRessourceFormatException() {
		super();
	}

	public IncorrectRessourceFormatException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public IncorrectRessourceFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public IncorrectRessourceFormatException(String message) {
		super(message);
	}

	public IncorrectRessourceFormatException(Throwable e) {
		super(e);
	}

}
